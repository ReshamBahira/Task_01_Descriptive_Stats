def extract_value(series, key="lower_bound"):
    def _parse(x):
        if pd.isna(x) or not str(x).strip().startswith("{"):
            return np.nan
        try:
            parsed = ast.literal_eval(str(x).strip())
            chosen = parsed.get(key) or parsed.get("lower_bound")
            return float(chosen) if chosen else np.nan
        except Exception:
            return np.nan
    return series.apply(_parse)


def build_range_columns(df):
    for name in RANGE_COLS:
        if name not in df.columns:
            continue
        df[name + "_lower"] = extract_value(df[name], "lower_bound")
        df[name + "_upper"] = extract_value(df[name], "upper_bound")
        df[name + "_mid"] = df.apply(
            lambda row: (row[name + "_lower"] + row[name + "_upper"]) / 2
            if not np.isnan(row.get(name + "_upper", np.nan))
            else row[name + "_lower"], axis=1
        )
    return df


def read_data(file_path):
    if not os.path.exists(file_path):
        print(f"[ERROR] Not found: {file_path}")
