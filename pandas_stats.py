def show_structure(df):
    print(f"\n{'═'*72}")
    print("  SECTION 1: DATASET STRUCTURE")
    print(f"{'═'*72}")
    print(f"\n  Shape   : {df.shape[0]:,} rows × {df.shape[1]} columns")
    print(f"  Memory  : {df.memory_usage(deep=True).sum() / 1024**2:.1f} MB")
    print(f"\n  Columns and dtypes:")
    for idx, name in enumerate(df.columns, 1):
        missing_count = df[name].isna().sum()
        missing_pct  = missing_count / len(df) * 100
        print(f"    {idx:>3}. {name:<52} {str(df[name].dtype):<10} "
              f"null: {missing_count:>7,} ({missing_pct:4.1f}%)")
    print(f"\n{DIVIDER}")
    print("  info()")
    print(DIVIDER)
    df.info()


def show_missing(df):
    print(f"\n{DIVIDER}")
    print("  SECTION 2: MISSING VALUES")
    print(DIVIDER)
    missing_total = df.isna().sum()
    missing_pct = df.isna().mean() * 100
    cell_count = df.size
    all_missing = missing_total.sum()
    print(f"\n  Total cells    : {cell_count:,}")
    print(f"  Missing cells  : {all_missing:,}  ({all_missing/cell_count*100:.1f}%)")
    print(f"  Rows with any  : {df.isna().any(axis=1).sum():,}")
    print(f"  Rows fully null: {df.isna().all(axis=1).sum():,}")
    print()
    missing_frame = pd.DataFrame({"missing": missing_total, "pct": missing_pct.round(1)})
    missing_frame = missing_frame[missing_frame["missing"] > 0].sort_values("missing", ascending=False)
    print(f"  {'Column':<52} {'Missing':>9} {'%':>7}")
    print(f"  {'─'*52} {'─'*9} {'─'*7}")
    for name, row in missing_frame.iterrows():
        print(f"  {name:<52} {int(row['missing']):>9,} {row['pct']:>6.1f}%")


