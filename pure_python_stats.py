def format_value(x, dec=2):
    if x is None:
        return "N/A"
    if isinstance(x, float):
        return f"{x:,.{dec}f}"
    return str(x)


def display_numeric(label, stats, total_count):
    pct_missing = stats["missing"] / total_count * 100 if total_count else 0
    print(f"\n  ┌ {label}  [numeric]")
    print(f"  │  non-null: {stats['count']:>9,}  missing: {stats['missing']:>7,} ({pct_missing:.1f}%)")
    print(f"  │  mean    : {format_value(stats['mean']):>14}   std    : {format_value(stats['std']):>14}")
    print(f"  │  min     : {format_value(stats['min']):>14}   max    : {format_value(stats['max']):>14}")
    print(f"  │  Q25     : {format_value(stats['q25']):>14}   median : {format_value(stats['median']):>14}   Q75: {format_value(stats['q75'])}")
    print(f"  └{'─'*60}")


def display_categorical(label, stats, total_count):
    pct_missing = stats["missing"] / total_count * 100 if total_count else 0
    print(f"\n  ┌ {label}  [categorical]")
    print(f"  │  non-null: {stats['count']:>9,}  missing: {stats['missing']:>7,} ({pct_missing:.1f}%)")
    print(f"  │  unique  : {stats['unique']:>9,}  mode   : {(str(stats['mode'])[:45] if stats['mode'] else 'N/A')}")
    print(f"  │  top values:")
    for val, cnt in stats["top_values"]:
        pct = cnt / stats["count"] * 100 if stats["count"] else 0
        shown = (str(val)[:55] + "…") if len(str(val)) > 56 else str(val)
        print(f"  │    {cnt:>8,}  ({pct:5.1f}%)  {shown}")
    print(f"  └{'─'*60}")


