def calc_mean(nums):
    return sum(nums) / len(nums) if nums else None


def calc_std_population(nums):
    if len(nums) < 2:
        return None
    avg = calc_mean(nums)
    return math.sqrt(sum((x - avg) ** 2 for x in nums) / len(nums))


def calc_median(nums):
    if not nums:
        return None
    sorted_vals = sorted(nums)
    size = len(sorted_vals)
    middle = size // 2
    return (sorted_vals[middle - 1] + sorted_vals[middle]) / 2.0 if size % 2 == 0 else float(sorted_vals[middle])


def calc_percentile(nums, pct):
    if not nums:
        return None
    sorted_vals = sorted(nums)
    index = (pct / 100.0) * (len(sorted_vals) - 1)
    lower_i, upper_i = int(index), int(index) + 1
    fraction = index - lower_i
    return sorted_vals[lower_i] if upper_i >= len(sorted_vals) else sorted_vals[lower_i] + fraction * (sorted_vals[upper_i] - sorted_vals[lower_i])


def summarize_numeric(column_values):
    """Full stats dict from a list of raw strings or floats."""
    clean_values, missing_count = [], 0
    for x in column_values:
        num, ok = to_float_safe(x)
        if ok:
            clean_values.append(num)
        else:
            missing_count += 1
    if not clean_values:
        return dict(count=0, missing=missing_count, mean=None, std=None,
                    median=None, min=None, max=None, q25=None, q75=None)
    return dict(
        count=len(clean_values), missing=missing_count,
        mean=calc_mean(clean_values), std=calc_std_population(clean_values),
        median=calc_median(clean_values), min=min(clean_values), max=max(clean_values),
        q25=calc_percentile(clean_values, 25), q75=calc_percentile(clean_values, 75),
    )


def summarize_categorical(column_values, top_n=5):
    filled_values, missing_count = [], 0
    for x in column_values:
        if value_is_missing(x):
            missing_count += 1
        else:
            filled_values.append(str(x).strip())
    if not filled_values:
        return dict(count=0, missing=missing_count, unique=0,
                    mode=None, mode_freq=0, top_values=[])
    freq_map = collections.Counter(filled_values)
    top_items = freq_map.most_common(top_n)
    return dict(count=len(filled_values), missing=missing_count, unique=len(freq_map),
                mode=top_items[0][0], mode_freq=top_items[0][1], top_values=top_items)


