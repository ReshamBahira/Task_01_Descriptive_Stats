def value_is_missing(x):
    return x is None or str(x).strip() in ("", "nan", "NaN", "None", "N/A", "NA")


def to_float_safe(x):
    """Return (float, True) or (None, False)."""
    if value_is_missing(x):
        return None, False
    try:
        return float(str(x).strip().replace(",", "")), True
    except ValueError:
        return None, False


def parse_range_text(text):
    """
    Parse Meta Ad Library dict-strings like:
      "{'lower_bound': '45000', 'upper_bound': '49999'}"
    Returns dict or None on failure.
    """
    if value_is_missing(text):
        return None
    try:
        return ast.literal_eval(text.strip())
    except Exception:
        return None


def get_range_bounds(text):
    """
    From a dict-string, return (lower_float, upper_float).
    If upper_bound missing (open-ended like ≥1,000,001), upper = lower.
    """
    parsed = parse_range_text(text)
    if parsed is None:
        return None, None
    lower_val, lower_ok = to_float_safe(parsed.get("lower_bound", ""))
    upper_val, upper_ok = to_float_safe(parsed.get("upper_bound", ""))
    lower = lower_val if lower_ok else None
    upper = upper_val if upper_ok else (lower if lower is not None else None)
    return lower, upper


