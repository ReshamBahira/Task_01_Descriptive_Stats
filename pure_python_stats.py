def read_dataset(file_path):
    if not os.path.exists(file_path):
        print(f"\n[ERROR] File not found: {file_path}")
        print("  Upload fb_ads_president_scored_anon.csv to Colab first.")
        return None, None, 0

    columns_data = collections.OrderedDict()
    row_count = 0
    print(f"  Reading: {file_path}  (this may take ~30 sec for 246K rows) …")

    with open(file_path, newline="", encoding="utf-8-sig") as f:
        reader = csv.DictReader(f)
        column_names = reader.fieldnames or []
        for col in column_names:
            columns_data[col] = []
        for row in reader:
            row_count += 1
            for col in column_names:
                columns_data[col].append(row.get(col, ""))
            if row_count % 50000 == 0:
                print(f"    … {row_count:,} rows loaded")

    print(f"  Done. {row_count:,} rows × {len(column_names)} columns")
    return column_names, columns_data, row_count


